import { promises as fs } from 'fs';
import * as path from 'path';
import { S3Client, GetObjectCommand } from '@aws-sdk/client-s3';

interface Document {
  id: string;
  fileName: string;
  originalFileName?: string;
  fileUrl?: string;
  documentType: string;
  uploadedAt: Date;
  status: string;
  fileSize: number;
}

interface Assessment {
  id: string;
  status: string;
  completedAt: Date | null;
  responses: AssessmentResponse[];
}

interface AssessmentResponse {
  questionId: number;
  questionText: string;
  category: string;
  score: number;
  answeredAt: Date;
}

/**
 * Generate comprehensive timeline analysis for client
 */
export async function generateTimelineAnalysis(client: any): Promise<string> {
  const healthEvents = [];

  // Add assessment events
  if (client.simpleAssessments) {
    for (const assessment of client.simpleAssessments) {
      if (assessment.completedAt) {
        healthEvents.push({
          date: assessment.completedAt,
          type: 'Assessment',
          event: `Functional Medicine Assessment Completed (${assessment.responses?.length || 0} questions)`,
          details: assessment.responses
            ? generateAssessmentSummary(assessment.responses)
            : 'Assessment data not available',
        });
      }
    }
  }

  // Add document events
  if (client.documents) {
    for (const document of client.documents) {
      healthEvents.push({
        date: document.uploadedAt,
        type: 'Document',
        event: `${document.documentType} uploaded: ${document.originalFileName || document.fileName}`,
        details: `File size: ${Math.round(document.fileSize / 1024)}KB, Status: ${document.status}`,
      });
    }
  }

  // Add protocol events
  if (client.protocols) {
    for (const protocol of client.protocols) {
      healthEvents.push({
        date: protocol.createdAt,
        type: 'Protocol',
        event: `Protocol Started: ${protocol.protocolName}`,
        details: `Status: ${protocol.status}, Supplements: ${Object.keys(protocol.supplements || {}).length}`,
      });
    }
  }

  // Add clinical note events
  if (client.notes) {
    for (const note of client.notes) {
      healthEvents.push({
        date: note.createdAt,
        type: 'Clinical Note',
        event: `${note.noteType} Session: ${note.title || 'Session Notes'}`,
        details:
          note.chiefComplaints || note.generalNotes || 'Clinical documentation',
      });
    }
  }

  // Sort events chronologically
  healthEvents.sort(
    (a, b) => new Date(a.date).getTime() - new Date(b.date).getTime()
  );

  return `# ${client.firstName} ${client.lastName} - Health Timeline Analysis

## 👤 Client Overview
- **Name**: ${client.firstName} ${client.lastName}
- **Email**: ${client.email}
- **Date of Birth**: ${client.dateOfBirth || 'Not specified'}
- **Gender**: ${client.gender || 'Not specified'}
- **Truck Driver**: ${client.isTruckDriver ? 'Yes' : 'No'}
- **Export Date**: ${new Date().toLocaleDateString()}

## 🏥 Health Goals
${client.healthGoals ? JSON.stringify(client.healthGoals, null, 2) : 'No health goals specified'}

## 💊 Current Medications
${client.medications ? JSON.stringify(client.medications, null, 2) : 'No medications listed'}

## 🩺 Medical Conditions
${client.conditions ? JSON.stringify(client.conditions, null, 2) : 'No conditions listed'}

## ⚠️ Allergies
${client.allergies ? JSON.stringify(client.allergies, null, 2) : 'No allergies listed'}

## 📅 Chronological Health Journey

${healthEvents
  .map(
    (event, index) => `
### ${index + 1}. ${event.event}
**Date**: ${new Date(event.date).toLocaleDateString()}  
**Type**: ${event.type}  
**Details**: ${event.details}
`
  )
  .join('\n')}

## 📊 Data Summary
- **Total Health Events**: ${healthEvents.length}
- **Assessments Completed**: ${client.simpleAssessments?.length || 0}
- **Documents Uploaded**: ${client.documents?.length || 0}
- **Active Protocols**: ${client.protocols?.filter((p: any) => p.status === 'active').length || 0}
- **Clinical Notes**: ${client.notes?.length || 0}

## 🎯 Assessment Performance Overview
${generateAssessmentOverview(client.simpleAssessments)}

## 📋 Protocol History
${generateProtocolSummary(client.protocols)}

---
*Generated by FNTP Comprehensive Export System on ${new Date().toISOString()}*
*This timeline provides chronological context for Claude Desktop protocol development*
`;
}

/**
 * Generate functional medicine assessment analysis
 */
export async function generateAssessmentAnalysis(
  assessments: Assessment[]
): Promise<string> {
  if (!assessments.length) {
    return `# Functional Medicine Assessment Analysis

## 📊 Assessment Status
No assessments have been completed for this client.

## 🎯 Recommendations
1. Complete the 80-question functional medicine assessment
2. Review health goals and concerns
3. Upload any relevant medical documents

---
*No assessment data available for analysis*
`;
  }

  const latestAssessment = assessments[0];
  const responses = latestAssessment.responses || [];

  // Analyze responses by category
  const categoryAnalysis = analyzeCategoryResponses(responses);
  const highestScores = responses
    .filter(r => r.score >= 4)
    .sort((a, b) => b.score - a.score)
    .slice(0, 10);

  const averageScore =
    responses.length > 0
      ? responses.reduce((sum, r) => sum + r.score, 0) / responses.length
      : 0;

  return `# Functional Medicine Assessment Analysis

## 📊 Assessment Overview
- **Assessment Date**: ${latestAssessment.completedAt?.toLocaleDateString() || 'In Progress'}
- **Questions Completed**: ${responses.length}/80
- **Completion Status**: ${latestAssessment.status}
- **Average Score**: ${averageScore.toFixed(1)}/5.0
- **High Priority Issues**: ${highestScores.length} (scores 4-5)

## 🎯 Category Analysis
${Object.entries(categoryAnalysis)
  .map(
    ([category, data]: [string, any]) => `
### ${category.toUpperCase()}
- **Average Score**: ${data.averageScore}/5.0
- **Question Count**: ${data.questionCount}
- **High Priority**: ${data.highPriorityCount} questions (score 4+)
- **Critical Concerns**: ${data.criticalCount} questions (score 5)
`
  )
  .join('')}

## 🚨 High Priority Findings (Score 4-5)
${
  highestScores.length > 0
    ? highestScores
        .map(
          (response, index) => `
${index + 1}. **${response.questionText}** (Score: ${response.score}/5)
   - Category: ${response.category}
   - Date: ${response.answeredAt.toLocaleDateString()}
`
        )
        .join('')
    : 'No high priority findings identified'
}

## 📈 Assessment Trends
${generateAssessmentTrends(assessments)}

## 🎯 Recommendations for Protocol Development
Based on the assessment patterns, the following areas require attention:

${generateProtocolRecommendations(categoryAnalysis, highestScores)}

---
*This analysis provides structured assessment data for Claude Desktop protocol development*
*Use this data alongside raw documents for comprehensive health analysis*
`;
}

