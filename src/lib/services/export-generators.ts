/**
 * Fetch document content from storage
 */
export async function fetchDocumentContent(
  document: Document
): Promise<Buffer | null> {
  try {
    // Handle S3 URLs
    if (document.fileUrl && document.fileUrl.includes('amazonaws.com')) {
      // For S3 URLs, we would need to use S3 client
      // For now, return null as we may not have proper S3 access
      return null;
    }

    // Handle local file storage
    if (document.fileUrl && document.fileUrl.startsWith('/')) {
      try {
        const filePath = path.join(process.cwd(), 'uploads', document.fileName);
        const buffer = await fs.readFile(filePath);
        return buffer;
      } catch (error) {
        console.error(`Local file not found: ${document.fileName}`);
        return null;
      }
    }

    // Handle HTTP URLs
    if (document.fileUrl && document.fileUrl.startsWith('http')) {
      const response = await fetch(document.fileUrl);
      if (response.ok) {
        return Buffer.from(await response.arrayBuffer());
      }
    }

    return null;
  } catch (error) {
    console.error('Error fetching document:', error);
    return null;
  }
}

/**
 * Analyze responses by category for pattern recognition
 */
function analyzeCategoryResponses(responses: AssessmentResponse[]) {
  const categories: Record<
    string,
    {
      scores: number[];
      averageScore: number;
      questionCount: number;
      highPriorityCount: number;
      criticalCount: number;
    }
  > = {};

  for (const response of responses) {
    const category = response.category || 'uncategorized';

    if (!categories[category]) {
      categories[category] = {
        scores: [],
        averageScore: 0,
        questionCount: 0,
        highPriorityCount: 0,
        criticalCount: 0,
      };
    }

    categories[category].scores.push(response.score);
    categories[category].questionCount++;
    if (response.score >= 4) categories[category].highPriorityCount++;
    if (response.score === 5) categories[category].criticalCount++;
  }

  // Calculate averages
  Object.values(categories).forEach(category => {
    category.averageScore =
      category.scores.length > 0
        ? Number(
            (
              category.scores.reduce((sum, score) => sum + score, 0) /
              category.scores.length
            ).toFixed(1)
          )
        : 0;
  });

  return categories;
}

/**
 * Generate assessment summary for timeline
 */
function generateAssessmentSummary(responses: AssessmentResponse[]): string {
  const averageScore =
    responses.reduce((sum, r) => sum + r.score, 0) / responses.length;
  const highPriorityCount = responses.filter(r => r.score >= 4).length;

  return `Average score: ${averageScore.toFixed(1)}/5.0, High priority issues: ${highPriorityCount}`;
}

/**
 * Generate assessment overview for timeline
 */
function generateAssessmentOverview(assessments: Assessment[]): string {
  if (!assessments || assessments.length === 0) {
    return 'No assessments completed';
  }

  const latest = assessments[0];
  const averageScore =
    latest.responses?.length > 0
      ? latest.responses.reduce((sum, r) => sum + r.score, 0) /
        latest.responses.length
      : 0;

  return `
### Latest Assessment (${latest.completedAt?.toLocaleDateString()})
- **Questions Answered**: ${latest.responses?.length || 0}/80
- **Average Health Score**: ${averageScore.toFixed(1)}/5.0
- **Status**: ${latest.status}
- **High Priority Issues**: ${latest.responses?.filter(r => r.score >= 4).length || 0}
`;
}

/**
 * Generate protocol summary
 */
function generateProtocolSummary(protocols: any[]): string {
  if (!protocols || protocols.length === 0) {
    return 'No protocols have been implemented';
  }

  return protocols
    .map(
      (protocol, index) => `
### ${index + 1}. ${protocol.protocolName}
- **Status**: ${protocol.status}
- **Created**: ${protocol.createdAt ? new Date(protocol.createdAt).toLocaleDateString() : 'Date unknown'}
- **Supplements**: ${Object.keys(protocol.supplements || {}).length} items
- **Dietary Guidelines**: ${protocol.dietary ? 'Included' : 'Not specified'}
- **Lifestyle Modifications**: ${protocol.lifestyle ? 'Included' : 'Not specified'}
`
    )
    .join('\n');
}

/**
 * Generate assessment trends analysis
 */
function generateAssessmentTrends(assessments: Assessment[]): string {
  if (assessments.length < 2) {
    return 'Insufficient data for trend analysis (need multiple assessments)';
  }

  const latest = assessments[0];
  const previous = assessments[1];

  if (!latest.responses || !previous.responses) {
    return 'Assessment data incomplete for trend analysis';
  }

  const latestAvg =
    latest.responses.reduce((sum, r) => sum + r.score, 0) /
    latest.responses.length;
  const previousAvg =
    previous.responses.reduce((sum, r) => sum + r.score, 0) /
    previous.responses.length;

  const change = latestAvg - previousAvg;
  const trend =
    change > 0.2 ? 'Worsening' : change < -0.2 ? 'Improving' : 'Stable';

  return `
### Trend Analysis (Last 2 Assessments)
- **Latest Average**: ${latestAvg.toFixed(1)}/5.0
- **Previous Average**: ${previousAvg.toFixed(1)}/5.0
- **Change**: ${change > 0 ? '+' : ''}${change.toFixed(1)}
- **Overall Trend**: ${trend}
`;
}

/**
 * Generate protocol recommendations based on assessment patterns
 */
function generateProtocolRecommendations(
  categoryAnalysis: any,
  highScores: AssessmentResponse[]
): string {
  const priorityCategories = Object.entries(categoryAnalysis)
    .filter(([_, data]: [string, any]) => data.averageScore >= 3.0)
    .sort((a, b) => (b[1] as any).averageScore - (a[1] as any).averageScore)
    .slice(0, 5);

  if (priorityCategories.length === 0) {
    return `
### Health Optimization Focus
- Overall assessment scores are in healthy range
- Focus on preventive care and optimization protocols
- Consider advanced functional testing for deeper insights
`;
  }

  return `
### Priority Treatment Areas
${priorityCategories
  .map(
    ([category, data]: [string, any]) => `
**${category.toUpperCase()}** (Score: ${data.averageScore}/5.0)
- ${data.questionCount} questions assessed
- ${data.highPriorityCount} high priority concerns
- ${data.criticalCount} critical issues
`
  )
  .join('')}

### Immediate Intervention Recommendations
${highScores
  .slice(0, 5)
  .map(
    (response, index) => `
${index + 1}. **${response.category}**: ${response.questionText} (${response.score}/5)
   - Requires targeted functional medicine intervention
   - Consider testing and targeted supplementation
`
  )
  .join('')}

### Protocol Development Priority
1. **Address Critical Findings**: Focus on scores of 5/5 first
2. **System-Based Approach**: Target highest-scoring categories
3. **Root Cause Investigation**: Look for underlying patterns
4. **Monitoring Plan**: Track improvements in high-priority areas
`;
}
